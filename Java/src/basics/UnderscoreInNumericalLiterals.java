package basics;

// We use underscore in numerical literals to enhance the readability.
// underscore are used for grouping the digits of a numerical literals to enhance the readability.
public class UnderscoreInNumericalLiterals {

	public static void main(String[] args) {
		
		int num = 10_00_000;
		System.out.println(num);
		
		float num1 = 5.00_77_845f;
		System.out.println(num1);
	}
}
